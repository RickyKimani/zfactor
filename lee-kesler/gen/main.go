package main

import (
	"encoding/json"
	"fmt"
	"log"
	"os"
	"strings"
)

type table struct {
	Pr     []float64   `json:"reduced_pressure"`
	Tr     []float64   `json:"reduced_temperature"`
	Values [][]float64 `json:"values"`
}

func main() {
	jsonFile := "../data/lee_kesler.json"
	outputFile := "tables.go"

	data, err := os.ReadFile(jsonFile)
	if err != nil {
		log.Fatal(err)
	}

	var tables map[string][]table
	err = json.Unmarshal(data, &tables)
	if err != nil {
		log.Fatal(err)
	}

	structCode := `// table represents a Lee/Kesler generalized correlation table
type table struct {
    Pr     []float64   //Reduced Pressure (x-axis)
    Tr     []float64   //Reduced Temperature (y-axis)
    Values [][]float64 //Values => f(Pr[i], Tr[j])
}
`

	var goCode strings.Builder
	goCode.WriteString(`// Code generated by go generate; DO NOT EDIT.

// package leekesler contains the Lee/Kesler generalized correlation 
// tables for the compressibility factor, residual enthalpy, 
// residual entropy and the fugacity coefficient.
package leekesler

// Lee-Kesler generalized correlation tables
// Generated from parsed PDF tables

` + structCode)

	var count int
	fmt.Println("#------------------------------------------------------#")

	descriptions := map[string]string{
		"z0":   "base compressibility factor for the lee/Kesler correlation (Z^0)",
		"z1":   "departure compressibility factor for the lee/Kesler correlation (Z^1)",
		"h0":   "base residual enthalpy for the lee/Kesler correlation ((H^R)^0/RTc)",
		"h1":   "departure residual enthalpy for the lee/Kesler correlation ((H^R)^1/RTc)",
		"s0":   "base residual entropy for the lee/Kesler correlation ((S^R)^0/R)",
		"s1":   "departure residual entropy for the lee/Kesler correlation ((S^R)^1/R)",
		"phi0": "base fugacity coefficient for the lee/Kesler correlation phi^0",
		"phi1": "departure fugacity coefficient for the lee/Kesler correlation phi^1",
	}

	for key, tableList := range tables {
		if len(tableList) == 0 {
			continue
		}
		// Expect exactly one combined table per key. If multiple exist, take the first.
		t := tableList[0]
		varName := strings.ToUpper(key) + "Table"
		fmt.Printf("Processing %s table\n", varName)

		if desc, ok := descriptions[strings.ToLower(key)]; ok {
			goCode.WriteString(fmt.Sprintf("// %s contains combined high pressure and low pressure data for the %s\n", varName, desc))
		}

		goCode.WriteString(fmt.Sprintf("var %s = &table{\n", varName))
		goCode.WriteString(fmt.Sprintf("\tPr: []float64{%s},\n", floatsToString(t.Pr)))
		goCode.WriteString(fmt.Sprintf("\tTr: []float64{%s},\n", floatsToString(t.Tr)))
		goCode.WriteString("\tValues: [][]float64{\n")
		for _, row := range t.Values {
			goCode.WriteString(fmt.Sprintf("\t\t{%s},\n", floatsToString(row)))
		}
		goCode.WriteString("\t},\n")
		goCode.WriteString("}\n\n")
		count++
	}

	fmt.Printf("Processed %d Lee/Kesler tables\n", count)
	fmt.Println("#------------------------------------------------------#")

	err = os.WriteFile(outputFile, []byte(goCode.String()), 0644)
	if err != nil {
		log.Fatal(err)
	}
}

func floatsToString(fs []float64) string {
	strs := make([]string, len(fs))
	for i, f := range fs {
		strs[i] = fmt.Sprintf("%.4f", f)
	}
	return strings.Join(strs, ", ")
}
